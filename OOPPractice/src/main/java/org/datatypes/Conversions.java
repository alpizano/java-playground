package org.datatypes;

public class Conversions {
    public static void main(String[] args) {
        char test = 'A';
        int test2 = (int)test;
        int lastRow = 'A' + 11;
        System.out.println(++test);
        System.out.println(test2);
        System.out.println(lastRow);
    }
}
